def get_spinner_verbs() -> list[str]:
    """Return verbs from settings when configured; else defaults."""
    from ..utils.settings.settings import get_merged_settings

    settings: dict[str, Any] = get_merged_settings()
    config = settings.get("spinnerVerbs")
    if not config or not isinstance(config, dict):
        return list(SPINNER_VERBS)
    mode = config.get("mode")
    extra = config.get("verbs")
    if not isinstance(extra, list):
        extra = []
    extra_strs = [str(x) for x in extra]
    if mode == "replace":
        return extra_strs if extra_strs else list(SPINNER_VERBS)
    return [*SPINNER_VERBS, *extra_strs]
