"""
Array utilities.

Migrated from: utils/array.ts — TS exports `intersperse`, `count`, `uniq` only;
this module adds common list helpers used across the Python port.
"""

from __future__ import annotations

from collections.abc import Callable, Iterable
from typing import TypeVar

T = TypeVar("T")
A = TypeVar("A")


def intersperse(items: list[A], separator_fn: Callable[[int], A]) -> list[A]:
    """
    Intersperse items with separators generated by a function.

    Args:
        items: List of items
        separator_fn: Function that takes index and returns separator

    Returns:
        List with separators interspersed
    """
    result: list[A] = []
    for i, item in enumerate(items):
        if i > 0:
            result.append(separator_fn(i))
        result.append(item)
    return result


def count(items: Iterable[T], predicate: Callable[[T], bool]) -> int:
    """
    Count items matching a predicate.

    Args:
        items: Iterable of items
        predicate: Function that returns True for items to count

    Returns:
        Count of matching items
    """
    return sum(1 for item in items if predicate(item))


def uniq(items: Iterable[T]) -> list[T]:
    """
    Return unique items preserving order.

    Args:
        items: Iterable of items

    Returns:
        List of unique items
    """
    seen = set()
    result = []
    for item in items:
        if item not in seen:
            seen.add(item)
            result.append(item)
    return result


def first(items: Iterable[T], default: T | None = None) -> T | None:
    """Get the first item or default."""
    for item in items:
        return item
    return default


def last(items: list[T], default: T | None = None) -> T | None:
    """Get the last item or default."""
    return items[-1] if items else default


def chunk(items: list[T], size: int) -> list[list[T]]:
    """
    Split a list into chunks of a given size.

    Args:
        items: List to chunk
        size: Size of each chunk

    Returns:
        List of chunks
    """
    return [items[i : i + size] for i in range(0, len(items), size)]


def flatten(nested: Iterable[Iterable[T]]) -> list[T]:
    """Flatten a nested iterable one level deep."""
    result = []
    for inner in nested:
        result.extend(inner)
    return result


def group_by(items: Iterable[T], key_fn: Callable[[T], str]) -> dict[str, list[T]]:
    """
    Group items by a key function.

    Args:
        items: Iterable of items
        key_fn: Function that returns the group key

    Returns:
        Dict mapping keys to lists of items
    """
    result: dict[str, list[T]] = {}
    for item in items:
        key = key_fn(item)
        if key not in result:
            result[key] = []
        result[key].append(item)
    return result


def partition(
    items: Iterable[T],
    predicate: Callable[[T], bool],
) -> tuple[list[T], list[T]]:
    """
    Partition items into two lists based on a predicate.

    Args:
        items: Iterable of items
        predicate: Function that returns True for first partition

    Returns:
        Tuple of (matching, non-matching) lists
    """
    matches = []
    non_matches = []
    for item in items:
        if predicate(item):
            matches.append(item)
        else:
            non_matches.append(item)
    return matches, non_matches


def find(
    items: Iterable[T],
    predicate: Callable[[T], bool],
) -> T | None:
    """Find the first item matching a predicate."""
    for item in items:
        if predicate(item):
            return item
    return None


def find_index(
    items: list[T],
    predicate: Callable[[T], bool],
) -> int:
    """Find the index of the first item matching a predicate, or -1."""
    for i, item in enumerate(items):
        if predicate(item):
            return i
    return -1


def compact(items: Iterable[T | None]) -> list[T]:
    """Remove None values from an iterable."""
    return [item for item in items if item is not None]


def take(items: Iterable[T], n: int) -> list[T]:
    """Take the first n items."""
    result = []
    for i, item in enumerate(items):
        if i >= n:
            break
        result.append(item)
    return result


def drop(items: list[T], n: int) -> list[T]:
    """Drop the first n items."""
    return items[n:]


def zip_with(
    a: list[A],
    b: list[T],
    fn: Callable[[A, T], any],
) -> list:
    """Zip two lists together with a combining function."""
    return [fn(x, y) for x, y in zip(a, b, strict=False)]
